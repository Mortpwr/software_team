def ensure_scholarship_catalog(db: Session) -> None:
    row = db.get(KnowledgeItem, POLICY_ID) or KnowledgeItem(id=POLICY_ID)
    row.title = "中国人民大学信息学院 2025 年综合类奖学金评审方案"
    row.category = "奖助政策"
    row.tags = ["奖学金", "国家奖学金", "国家励志奖学金", "综合类奖学金", "荣誉评审"]
    row.summary = POLICY_SUMMARY
    row.body = POLICY_BODY
    row.official_link = ""
    row.sensitive_hint = False
    row.online = True
    db.add(row)

    for item in SCHOLARSHIP_HONORS:
        honor = db.get(Honor, item["id"]) or Honor(id=item["id"])
        honor.title = item["title"]
        honor.winner = item["winner"]
        honor.year = item["year"]
        honor.major = item["major"]
        honor.grade = item["grade"]
        honor.category = item["category"]
        honor.intro = item["intro"]
        honor.visibility = "public"
        honor.online = True
        honor.attachments = []
        db.add(honor)

